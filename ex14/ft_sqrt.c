int		ft_sqrt(int nb)
{
	int i;
	int carre;

	i = 1;
	if (nb < 1)
		return (0);
	while ((i * i) <= nb)
	{
		carre = i * i;
		if (carre == nb)
			return (i);
		i++;
	}
	return (0);
}
